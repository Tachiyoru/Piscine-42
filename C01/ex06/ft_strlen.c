#include <unistd.h>

int	ft_strlen(char *str)
{
	int	a;
	int	cmptr;

	cmptr = 0;
	a = 0;
	while (str[a] != '\0')
	{
		a++;
		cmptr++;
	}
	return (cmptr);
}
